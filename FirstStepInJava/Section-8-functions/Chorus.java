public class Chorus {
    public static void main(String[] args) {
        singChorus();
        singChorus();
        singChorus();
    }

    public static void singChorus(){
        System.out.println("C'est l'hymne de nos campagnes");
        System.out.println("De nos rivieres de nos montagnes");
        System.out.println("C'est la vie man, du monde animal");
        System.out.println("\n");
    }
}
